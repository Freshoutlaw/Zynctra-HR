import os
import sys
from pathlib import Path

def create_directory(path):
    """Create directory if it doesn't exist."""
    Path(path).mkdir(parents=True, exist_ok=True)

def write_file(filepath, content=""):
    """Write content to a file, creating parent directories if needed."""
    create_directory(Path(filepath).parent)
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)

def create_placeholder_file(filepath, extension="txt"):
    """Create a placeholder file with basic content."""
    content = f"""// Placeholder file: {Path(filepath).name}
// This file is part of the Zynctra HR Platform
// Auto-generated by setup script

// TODO: Implement this module

export default {{}};
"""
    if extension == "java":
        content = f"""// Placeholder: {Path(filepath).name}
package com.zynctra;

public class {Path(filepath).stem} {{
    // TODO: Implement this class
}}
"""
    elif extension == "py":
        content = f'''"""Placeholder: {Path(filepath).name}"""
# TODO: Implement this module

def main():
    pass

if __name__ == "__main__":
    main()
'''
    elif extension == "md":
        content = f"# {Path(filepath).stem}\n\nTODO: Add documentation for this component.\n"
    elif extension == "yml":
        content = f"# Placeholder configuration: {Path(filepath).name}\n"
    elif extension == "tf":
        content = f"# Terraform configuration: {Path(filepath).name}\n"
    elif extension == "tsx":
        content = f"""// Placeholder: {Path(filepath).name}
import React from 'react';

const {Path(filepath).stem}: React.FC = () => {{
  return <div>Placeholder Component</div>;
}};

export default {Path(filepath).stem};
"""
    elif extension == "ts":
        content = f"// Placeholder: {Path(filepath).name}\nexport const placeholder = true;\n"
    elif extension == "css":
        content = f"/* Placeholder: {Path(filepath).name} */\n"
    elif extension == "json":
        content = "{{\n  \"placeholder\": true\n}}\n"
    elif extension == "sh":
        content = f"""#!/bin/bash
# Placeholder script: {Path(filepath).name}
echo "Running {Path(filepath).name}"
"""
    elif extension == "ps1":
        content = f"""# Placeholder script: {Path(filepath).name}
Write-Host "Running {Path(filepath).name}"
"""
    elif extension == "html":
        content = f"""<!DOCTYPE html>
<html>
<head><title>{Path(filepath).stem}</title></head>
<body>Placeholder</body>
</html>
"""
    else:
        content = f"// Placeholder: {Path(filepath).name}\n"
    
    write_file(filepath, content)

